class Product {
  final int id;
  final String title;
  final int reviews;
  final int price;
  final String description;
  final int controller;
  final int games;
  final String imagePath;

  Product({
    required this.id,
    required this.title,
    required this.reviews,
    required this.price,
    required this.description,
    required this.controller,
    required this.games,
    required this.imagePath,
  });
}
